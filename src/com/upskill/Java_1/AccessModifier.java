public class AccessModifier {
	
 public String name = "Faria";       //public- any class can access
 
 private int age = 25;               //private- only same class can access
 
 protected int ssn = 12345;          //protected- classes in the same package and subclass can access it
 
     String address = "Mckee Street";    // default- classes in same package can access.
 
}
