//5.  Write a program in Java to display the multiplication table of (eight)8.

public class Assignment2_5 {

	public static void main(String[] args) {
		int a;
	
		for(a=1; a<=10;a++){
			
			int multiplication = a*8;
			System.out.print("  " +      a);
			System.out.print(" * 8 ");
			System.out.println(" = "+ multiplication);
		}

	}

	}
